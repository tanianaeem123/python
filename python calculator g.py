a=int(input("Enter a first number:  "))

b=int(input("Enter a second number:  "))

oper=input("Enter a operator:  ")

if oper=="+":
    add=a+b
    print(add)

if oper=="-":
    sub=a-b
    print(sub)

if oper=="*":
    multi=a*b
    print(multi)

if oper=="/":
    divI=a/b
    print(divI)
    
    
